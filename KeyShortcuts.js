window.addEventListener("keydown", homePage, false);
function homePage(key){
    if (key.keyCode == "20"){
window.location.href = 'https://www.mahwos92.tk/';
    }
}
window.addEventListener("keydown", topPage, false);
function topPage(key){
    if (key.keyCode == "16"){
window.location.href = '#';
    }
}
